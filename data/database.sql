BEGIN TRANSACTION;

CREATE TABLE "contact" (
    "id" integer NOT NULL,
    "last_name" varchar NOT NULL DEFAULT NULL,
    "first_name" varchar DEFAULT NULL,
    "phone" varchar DEFAULT NULL,
    "email" varchar DEFAULT NULL,
    PRIMARY KEY (id)
);

CREATE TABLE "product" (
    "id" integer NOT NULL,
    "name" varchar NOT NULL,
    "duration" integer NOT NULL,
    PRIMARY KEY (id)
);

CREATE TABLE "agent" (
    "id" integer,
    "username" varchar,
    "manager_id" integer,
    PRIMARY KEY (id),
    FOREIGN KEY ("manager_id") REFERENCES "agent"("id")
);

CREATE TABLE "team" (
    "id" integer,
    "name" varchar,
    "manager_id" integer,
    PRIMARY KEY (id),
    FOREIGN KEY ("manager_id") REFERENCES "agent"("id")
);

CREATE TABLE "agent_team" (
    "id" integer,
    "agent_id" integer,
    "team_id" integer,
    PRIMARY KEY (id),
    FOREIGN KEY ("agent_id") REFERENCES "agent"("id"),
    FOREIGN KEY ("team_id") REFERENCES "team"("id")
);

CREATE TABLE "activity" (
    "id" integer,
    "type" varchar NOT NULL,
    "activity_datetime" datetime NOT NULL,
    "contact_id" integer NOT NULL,
    "agent_id" integer NOT NULL,
    PRIMARY KEY (id),
    FOREIGN KEY ("contact_id") REFERENCES "contact"("id"),
    FOREIGN KEY ("agent_id") REFERENCES "agent"("id")
);

CREATE TABLE "session" (
    "id" integer NOT NULL,
    "amount" float NOT NULL,
    "status" varchar NOT NULL,
    "starting_date" varchar,
    "ending_date" varchar,
    "contact_id" integer,
    "product_id" integer,
    "owner_id" integer,
    PRIMARY KEY (id),
    FOREIGN KEY ("contact_id") REFERENCES "contact"("id"),
    FOREIGN KEY ("product_id") REFERENCES "product"("id"),
    FOREIGN KEY ("owner_id") REFERENCES "agent"("id")
);

CREATE TABLE "target" (
    "id" integer NOT NULL,
    "date" date NOT NULL,
    "amount" float NOT NULL,
    "owner_id" integer,
    PRIMARY KEY (id),
    FOREIGN KEY ("owner_id") REFERENCES "agent"("id")
);

CREATE TABLE "target_operation" (
    "id" integer NOT NULL,
    "amount" float NOT NULL,
    "target_id" integer,
    "session_id" integer,
    PRIMARY KEY (id),
    FOREIGN KEY ("target_id") REFERENCES "target"("id"),
    FOREIGN KEY ("session_id") REFERENCES "session"("id")
);

